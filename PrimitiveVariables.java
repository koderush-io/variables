public class PrimitiveVariables{

    public static void main(String ... args ){

        System.out.println("Primitive variable app has started!");

        char singleCharacterVariable = 'A';
        System.out.println("char value = "+singleCharacterVariable);

        short shortTypeVariable = (short)2000.22;
        System.out.println("short value = "+shortTypeVariable);

        int integerVariable = (int)500.22;
        System.out.println("int value = "+integerVariable);

        long longVariable = 7777777L;
        System.out.println("long value = "+longVariable);

        float floatVariable = 234.55f;
        System.out.println("float value = "+floatVariable);

        double doubleVariable = 334.55;
        System.out.println("double value = "+doubleVariable);

        boolean booleanVariable = false;
        System.out.println("boolean value = "+booleanVariable);


    }

}